#include <stdio.h>

//This tasks involves the usage of four different arithmetic operators between 5 & 2.

int main()
{
	printf("%d %d %d\n", 5, 2, 5+2);
	printf("%d + %d = %d\n", 5, 2, 5+2);
	printf("%d - %d = %d\n", 5, 2,  5-2);
	printf("%d * %d = %d\n", 5, 2, 5*2);
	printf("%d / %d = %d\n", 5, 2, 5/2);
	return 0;
}
